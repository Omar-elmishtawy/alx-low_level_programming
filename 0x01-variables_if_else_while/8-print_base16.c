#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - get last digit of a number.
 * Description: this get the last digit
 * and checks if it greater than five
 * or less than six.
 * Return: Stops the program
 */
int main(void)
{
	int let;

	for (let = 48; let <= 57; let++)
		putchar(let);
	for (let = 97; let <= 102; let++)
		putchar(let);
	putchar('\n');
	return (0);
}
