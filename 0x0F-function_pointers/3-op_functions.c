#include "3-calc.h"

/**
 * op_add - add
 * @a: a
 * @b: b
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub
 * @a: a
 * @b: b
 * Return: jsfalf
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 * @a: a
 * @b: b
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - dsad
 * @a: a
 * @b: b
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_div - div
 * @a: a
 * @b: b
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
